"""
Schema, I/O, and validation for the evaluation dataset (data/eval/eval.jsonl).

WHY THIS FILE EXISTS:
    The eval set is the most important file in the project — it's the "ruler"
    every score is measured against. We need:
      1. A strict schema so every row has the same fields.
      2. A validator that catches mistakes (missing fields, empty answers,
         duplicate IDs) BEFORE training, not during.
      3. A loader/saver so other scripts read the same way.

JSONL FORMAT (NOT JSON):
    Each line is its own self-contained JSON object. Example file:

        {"id": "docker-001", "question": "...", "reference_answer": "...", ...}
        {"id": "docker-002", "question": "...", "reference_answer": "...", ...}

    Why JSONL not JSON:
      - You can append a new example without re-parsing the whole file.
      - You can stream-read it without loading everything into memory.
      - Standard format for ML datasets (HuggingFace, OpenAI all use JSONL).
"""

from __future__ import annotations

import json
from dataclasses import dataclass, asdict, field
from pathlib import Path
from typing import Iterable

# Valid values for the `category` and `difficulty` fields. Defined here so the
# validator can reject typos. If we want to add a new category, we add it here
# (one place) instead of hunting for hardcoded strings across the codebase.
VALID_CATEGORIES = {
    "commands",          # docker run / ps / logs / exec / etc.
    "dockerfile",        # FROM / RUN / CMD / COPY / multi-stage / etc.
    "networking",        # ports, networks, DNS, --network
    "volumes",           # bind mounts, named volumes, persistence
    "compose",           # docker-compose.yml syntax & usage
    "troubleshooting",   # common errors & their fixes
    "images",            # pull, push, tag, layers, registries
    "security",          # USER, secrets, --read-only, .dockerignore
}

VALID_DIFFICULTIES = {"easy", "medium", "hard"}


# -----------------------------------------------------------------------------
# Schema: what a single eval example looks like
# -----------------------------------------------------------------------------
@dataclass
class EvalExample:
    """One row of the eval dataset.

    Fields:
        id                : unique stable ID, e.g. "docker-001". Stable means
                            we never reassign IDs — adding example #51 doesn't
                            shift IDs of #1..#50.
        question          : the question we ask the model.
        reference_answer  : the verified correct answer (from authoritative
                            source). NEVER generated by an LLM.
        category          : one of VALID_CATEGORIES.
        difficulty        : "easy" | "medium" | "hard".
        source            : URL or document the reference answer was taken
                            from. Critical for audit — without it we can't
                            prove the answer was human-verified.
        notes             : optional free-text (e.g. "added by hand 2026-06-21").
    """
    id:               str
    question:         str
    reference_answer: str
    category:         str
    difficulty:       str
    source:           str
    notes:            str = ""


# -----------------------------------------------------------------------------
# I/O — load and save the JSONL file
# -----------------------------------------------------------------------------
def load_eval(path: Path) -> list[EvalExample]:
    """Read eval.jsonl into a list of EvalExample objects.

    Skips blank lines so the file can have visual gaps for readability.
    Raises a clear error if any line is malformed JSON.
    """
    examples = []
    with open(path, "r", encoding="utf-8") as f:
        for line_num, line in enumerate(f, start=1):
            line = line.strip()
            if not line:
                continue
            try:
                row = json.loads(line)
            except json.JSONDecodeError as e:
                raise ValueError(
                    f"{path}:{line_num} is not valid JSON: {e}"
                ) from None
            examples.append(EvalExample(**row))
    return examples


def save_eval(examples: Iterable[EvalExample], path: Path) -> None:
    """Write examples to JSONL.

    Sorts by ID first so the file is stable across runs (helps git diffs).
    """
    sorted_examples = sorted(examples, key=lambda e: e.id)
    path.parent.mkdir(parents=True, exist_ok=True)
    with open(path, "w", encoding="utf-8") as f:
        for ex in sorted_examples:
            # ensure_ascii=False lets us keep Unicode characters readable
            # (matters if we ever extend to Arabic/other languages later).
            f.write(json.dumps(asdict(ex), ensure_ascii=False) + "\n")


# -----------------------------------------------------------------------------
# Validation — catch mistakes BEFORE we run baseline eval
# -----------------------------------------------------------------------------
@dataclass
class ValidationReport:
    """Result of validating an eval set.

    `errors` are blocking (file is broken).
    `warnings` are non-blocking (file works but might be lower quality).
    """
    errors:   list[str] = field(default_factory=list)
    warnings: list[str] = field(default_factory=list)

    @property
    def ok(self) -> bool:
        return len(self.errors) == 0


def validate_eval(examples: list[EvalExample]) -> ValidationReport:
    """Check the eval set for common mistakes.

    Errors (must fix):
      - Duplicate IDs.
      - Empty question, answer, or source.
      - Invalid category or difficulty.
      - Reference answer that looks like a placeholder ("TODO", "FILL_ME", "").

    Warnings (should consider):
      - Fewer than 50 examples.
      - Imbalanced difficulty (e.g. all easy).
      - Imbalanced categories (one category dominates).
      - Very short reference answers (<20 chars) which often signals
        copy-paste mistakes.
    """
    report = ValidationReport()

    if not examples:
        report.errors.append("Eval set is empty.")
        return report

    # --- ERRORS -------------------------------------------------------------
    seen_ids: dict[str, int] = {}
    for ex in examples:
        # Duplicate IDs would silently overwrite results during scoring.
        if ex.id in seen_ids:
            report.errors.append(f"Duplicate ID: {ex.id!r}")
        seen_ids[ex.id] = seen_ids.get(ex.id, 0) + 1

        if not ex.question.strip():
            report.errors.append(f"{ex.id}: empty question.")

        if not ex.reference_answer.strip():
            report.errors.append(f"{ex.id}: empty reference_answer.")

        if ex.reference_answer.strip().upper() in {"TODO", "FILL_ME", "FILLME", "TBD"}:
            report.errors.append(
                f"{ex.id}: reference_answer is a placeholder "
                f"({ex.reference_answer!r}). Fill in the verified answer."
            )

        if not ex.source.strip():
            report.errors.append(
                f"{ex.id}: missing source URL — every answer must cite where "
                "it was verified from."
            )

        if ex.category not in VALID_CATEGORIES:
            report.errors.append(
                f"{ex.id}: invalid category {ex.category!r}. "
                f"Must be one of {sorted(VALID_CATEGORIES)}."
            )

        if ex.difficulty not in VALID_DIFFICULTIES:
            report.errors.append(
                f"{ex.id}: invalid difficulty {ex.difficulty!r}. "
                f"Must be one of {sorted(VALID_DIFFICULTIES)}."
            )

    # --- WARNINGS -----------------------------------------------------------
    if len(examples) < 50:
        report.warnings.append(
            f"Only {len(examples)} examples. Weekend target is 50."
        )

    # Difficulty balance: each tier should make up at least 15% of the set.
    diff_counts = {d: 0 for d in VALID_DIFFICULTIES}
    for ex in examples:
        if ex.difficulty in diff_counts:
            diff_counts[ex.difficulty] += 1
    for diff, count in diff_counts.items():
        pct = count / len(examples) * 100
        if pct < 15:
            report.warnings.append(
                f"Only {count} {diff} examples ({pct:.0f}%). "
                "Target: each difficulty ≥15%."
            )

    # Category balance: no single category should dominate (>50%).
    cat_counts: dict[str, int] = {}
    for ex in examples:
        cat_counts[ex.category] = cat_counts.get(ex.category, 0) + 1
    for cat, count in cat_counts.items():
        pct = count / len(examples) * 100
        if pct > 50:
            report.warnings.append(
                f"Category {cat!r} is {pct:.0f}% of the set — too dominant. "
                "Spread questions across more categories."
            )

    # Short-answer check — often a sign of incomplete entries.
    for ex in examples:
        if len(ex.reference_answer.strip()) < 20:
            report.warnings.append(
                f"{ex.id}: reference_answer is very short "
                f"({len(ex.reference_answer)} chars). Possibly incomplete."
            )

    return report


def summarize_eval(examples: list[EvalExample]) -> dict:
    """Return a small dict of stats (used by 01_build_eval.py for printing)."""
    stats: dict = {
        "total": len(examples),
        "by_category": {},
        "by_difficulty": {},
    }
    for ex in examples:
        stats["by_category"][ex.category] = (
            stats["by_category"].get(ex.category, 0) + 1
        )
        stats["by_difficulty"][ex.difficulty] = (
            stats["by_difficulty"].get(ex.difficulty, 0) + 1
        )
    return stats
